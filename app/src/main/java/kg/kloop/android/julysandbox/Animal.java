package kg.kloop.android.julysandbox;

public class Animal {
    private String paws;
    private String teeth;
    private String fur;

    Animal(){
    }

    public void run() {

    }

    public String getPaws() {
        return paws;
    }

    public void setPaws(String paws) {
        this.paws = paws;
    }

    public String getTeeth() {
        return teeth;
    }

    public void setTeeth(String teeth) {
        this.teeth = teeth;
    }

    public String getFur() {
        return fur;
    }

    public void setFur(String fur) {
        this.fur = fur;
    }
}
